function solution(m, k, arr) {
  let result = 0;
  arr.sort((a, b) => b - a); // [6, 5, 4, 4, 2]

  while (true) {
    for (let i = 0; i < k; i++) {
      if (m === 0) {
        break;
      }
      result += arr[0];
      m--;
    }

    if (m === 0) {
      break;
    }

    result += arr[1];
    m--;
  }

  console.log(result);
}

